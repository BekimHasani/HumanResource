    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        int row = table.getSelectedRow();
        
        if(row != -1){
           
                TrainingTypes tt = tttm.getTrainingTypes(row);
                tt.setTrainingDescription(trainingDescriptionTxt.getText());
                tt.setCertifiable(isCertifiable);
                if(tt.getTrainingDescription().isEmpty() ){
                    JOptionPane.showMessageDialog(null, "Description cannot be empty");
                }else{
                    try{
                        ttr.edit(tt);
                        clear();
                        tableLoad();
                    }
                    catch(TrainingTypesException tte){
                        JOptionPane.showMessageDialog(this, "Update * " + tte.getMessage());
                    }
                }
            }else{
            JOptionPane.showMessageDialog(null, "Please select the item you want to edit");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed

        int row = table.getSelectedRow();

        if (row == -1) {
            TrainingTypes tt = new TrainingTypes();

            tt.setTrainingDescription(trainingDescriptionTxt.getText());
            tt.setCertifiable(isCertifiable);


            if (tt.getTrainingDescription().isEmpty() || !Utility.validName(tt.getTrainingDescription())) {
                JOptionPane.showMessageDialog(null, "Description cannot be empty");
            } else {
                try {
                    ttr.create(tt);
                    clear();
                    tableLoad();
                } catch (TrainingTypesException tte) {
                    JOptionPane.showMessageDialog(this, "Save " + tte.getMessage());
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Fillout the required fields before saving");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int row = table.getSelectedRow();
        
        if(row != -1){
            Object[] obj = {"Yes", "No"};
            
            int i = JOptionPane.showOptionDialog(this, "Do you want to delete the selected item", "Deleted succesfully",
                   JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, obj, obj[1]);
            if(i == 0){
                
                TrainingTypes tt = tttm.getTrainingTypes(row);
                try{
                    ttr.remove(tt);
                    clear();
                    tableLoad();
                }catch(TrainingTypesException tte){
                    JOptionPane.showMessageDialog(null, "Delete exception: " + tte.getMessage());
                }
                
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select the item you want to delete");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

