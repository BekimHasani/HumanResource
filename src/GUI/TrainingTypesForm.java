    public void tableLoad() {
        try {
            List<TrainingTypes> list = ttr.findAll();
            tttm = new TrainingTypesTableModel(list);
            table.setModel(tttm);
            tttm.fireTableDataChanged();
            tabelaSelectedIndexChange();
        } catch (TrainingTypesException tte) {
            JOptionPane.showMessageDialog(this, "TSIC method: " + tte.getMessage());
        }
    }

    public void clear() {

        table.clearSelection();
        trainingDescriptionTxt.setText("");
    }

    public void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = table.getSelectionModel();

        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (e.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    TrainingTypes tt = tttm.getTrainingTypes(selectedIndex);

                    trainingDescriptionTxt.setText(tt.getTrainingDescription());
                    if (tt.getCertifiable()) {
                        certifiableRBtn.setSelected(true);
                    } else {
                        notcertifiableRBtn.setSelected(true);
                    }

                }
            }

        });

    }

