    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            Holidays h = new Holidays();
            h.setHolidayDescription(holidaysDescriptionTxt.getText());
            h.setStartDate(startDateChooser.getDate());
            h.setEndDate(endDateChooser.getDate());

            if (h.getHolidayDescription().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Description is required ");
            } else if (!Utility.validName(holidaysDescriptionTxt.getText())) {
                JOptionPane.showMessageDialog(null, "Description can only contain letters");
            } else {
                hr.create(h);
                clearFields();
                tableLoad();
                JOptionPane.showMessageDialog(this, "Saved succesfully");
            }
        } catch (HolidaysException he) {
            JOptionPane.showMessageDialog(this, he.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed
    private void tableLoad() {

        try {
            List<Holidays> list = hr.findAll();
            htm = new HolidaysTableModel(list);
            table.setModel(htm);
            htm.fireTableDataChanged();
            tabelaSelectedIndexChanged();
        } catch (HolidaysException ex) {
            Logger.getLogger(HolidaysFormm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearFields() {
        table.clearSelection();
        holidaysDescriptionTxt.setText("");
        startDateChooser.setDate(null);
        endDateChooser.setDate(null);

    }

    private void tabelaSelectedIndexChanged() {
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Holidays h = htm.getHolidays(selectedIndex);
                    holidaysDescriptionTxt.setText(h.getHolidayDescription());
                    startDateChooser.setDate(h.getStartDate());
                    endDateChooser.setDate(h.getEndDate());

                }
            }
        });
    }


