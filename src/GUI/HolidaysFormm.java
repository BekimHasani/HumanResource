    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int row = table.getSelectedRow();

        if (row != -1) {
            Object[] ob = {"Po", "Jo"};
            int i = javax.swing.JOptionPane.showOptionDialog(this, "A dëshironi ta fshini ?", "Fshirja", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);

            if (i == 0) {
                Holidays h = this.htm.getHolidays(row);
                try {
                    hr.remove(h);
                } catch (HolidaysException ex) {
                    JOptionPane.showMessageDialog(null, "Gabim diqka");
                }
                tableLoad();
                clearFields();
                JOptionPane.showMessageDialog(this, "E dhëna është fshire me sukses !");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Duhet te selektoni statusi qe doni te fshini");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        int row = table.getSelectedRow();

        if (row != -1) {
            Holidays h = this.htm.getHolidays(row);
            h.setHolidayDescription(holidaysDescriptionTxt.getText());
            h.setStartDate(startDateChooser.getDate());
            h.setEndDate(endDateChooser.getDate());

            if (h.getHolidayDescription().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Description is required ");
            } else if (!Utility.validName(holidaysDescriptionTxt.getText())) {
                JOptionPane.showMessageDialog(null, "Description can only contain letters");
            } else {
                try {
                    hr.edit(h);
                    clearFields();
                    tableLoad();
                } catch (HolidaysException pex) {
                    JOptionPane.showMessageDialog(this, "Update : " + pex.getMessage());
                }
                clearFields();
                tableLoad();
                JOptionPane.showMessageDialog(this, "Updated succesfully");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Duhet selektuar nje fushe para se te editoni");
        }
    }//GEN-LAST:event_updateBtnActionPerformed

