    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        try {
            String username = usernameTxt.getText();
            String password = new String(Utility.getMD5(passwordField.getText()));
            HumanResourceLoginRepository hrr = new HumanResourceLoginRepository();
            HumanResourceLogin hr = hrr.loginByUsernameAndPassword(username, password);
            if (hr != null) {
                try {
                    Integer loginID = lr.findLogin(hr.getEmployee().getEmployeeID());
                    if (loginID != null) {
                        Logins l = lr.findById(loginID);
                        Time current = new Time(System.currentTimeMillis());
                        l.setLogouted(current);
                        Time lastLogin = new Time(l.getLogined().getTime());
                        l.setLoginedTime(new Time(l.getLogouted().getHours() - lastLogin.getHours(), l.getLogouted().getMinutes() - lastLogin.getMinutes(),
                                l.getLogouted().getSeconds() - lastLogin.getSeconds()));
                        System.out.println("Problems fixed");
                        lr.edit(l);
                    }
                } catch (LoginsException ex) {
                    System.out.println("No problems");
                }
                if (hr.getEmployee() == null) {
                    JOptionPane.showMessageDialog(this,"[Login-ERROR] : Username or password is wrong or doesnt exist");
                }else{
                    Time loginedTime = Create(hr.getEmployee());
                    new MainForm(hr.getEmployee(), loginedTime).setVisible(true);
                    this.dispose();
                }
            }
        } catch (HumanResourceLoginException ex) {
            JOptionPane.showMessageDialog(this, "[HumanResourceLoginException]:->LoginForm " + ex.getMessage());
        }
    }//GEN-LAST:event_loginBtnActionPerformed

