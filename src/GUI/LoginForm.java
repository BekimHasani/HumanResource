    public Time Create(Employee e) {
        Logins log = new Logins();
        log.setEmployee(e);
        log.setLoginDate(new java.util.Date());
        LocalTime localLoginedTime = LocalTime.now();
        Time loginedTime = Time.valueOf(localLoginedTime);
        log.setLogined(loginedTime);
        try {
            lr.create(log);
        } catch (LoginsException ex) {
            JOptionPane.showMessageDialog(this, "[LoginForm]->LR-create" + ex.getMessage());
        }
        return loginedTime;
    }

