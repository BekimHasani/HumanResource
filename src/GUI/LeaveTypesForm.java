    public void loadTable() {
        try {
            lttm.addList(ltr.findAll());
            leaveTypesTbl.setModel(lttm);
            lttm.fireTableDataChanged();
        } catch (LeaveTypesException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

    public void clear() {
        leaveTypesTbl.clearSelection();
        leaveIdTxt.setText("");
        leaveDescriptionTxtArea.setText("");
        leaveDaysTxt.setText("");
        activeRBtn.setSelected(false);
        inactiveRBtn.setSelected(false);
    }

    private void tableSelectedIndexChanged() {
        final ListSelectionModel rowSM = leaveTypesTbl.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    LeaveTypes lt = lttm.getLeaveTypes(selectedIndex);
                    leaveIdTxt.setText(lt.getLeaveID() + "");
                    leaveDescriptionTxtArea.setText(lt.getLeaveDescription());
                    leaveDaysTxt.setText(lt.getLeaveDays() + "");
                    if (lt.getLeaveStatus()) {
                        activeRBtn.setSelected(true);;
                    } else {
                        inactiveRBtn.setSelected(false);
                    }
                    
                    
                }
            }
        });
    }

