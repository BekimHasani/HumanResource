    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        int row = leaveTypesTbl.getSelectedRow();
        if (row == -1) {
            LeaveTypes lt = new LeaveTypes();
            lt.setLeaveDescription(leaveDescriptionTxtArea.getText());
            lt.setLeaveDays((short) Integer.parseInt(leaveDaysTxt.getText()));
            lt.setLeaveStatus(status);
            if (!leaveDescriptionTxtArea.getText().equals("") && !leaveDaysTxt.getText().isEmpty()) {
                try {
                    ltr.create(lt);
                    loadTable();
                    clear();
                } catch (LeaveTypesException ex) {
                    JOptionPane.showMessageDialog(this, "MSG: /n" + ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Plotesoni fushat obligative!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Stausi egzsiston!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int row = leaveTypesTbl.getSelectedRow();
        if (row != -1) {
            LeaveTypes lt = lttm.getLeaveTypes(row);
            lt.setLeaveDescription(leaveDescriptionTxtArea.getText());
            lt.setLeaveDays((short) Integer.parseInt(leaveDaysTxt.getText()));
            lt.setLeaveStatus(status);
            if (!leaveDescriptionTxtArea.getText().isEmpty() && !leaveDaysTxt.getText().isEmpty()) {
                try {
                    ltr.edit(lt);
                    clear();
                    loadTable();
                } catch (LeaveTypesException ex) {
                    JOptionPane.showMessageDialog(this, "MSG: /n" + ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Mbusheni hapsirat me te dhena..");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Selektoni statusin qe deshironi ta editoni..");
        }
    }//GEN-LAST:event_editBtnActionPerformed

